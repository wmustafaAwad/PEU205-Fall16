function dy = waleed(t,y)
    x1 = 0;    x2 = 5;
    y1 = 0;    y2 = 0;  
    M1 = 10;    M2 =  10;
    Om = 0.1;
    
    
    dy(2) = - M1*(y(1)-x1)/((y(1)-x1)^2 + (y(3)-y1)^2+y(5)^2)^(3/2) ...
            - M2*(y(1)-x2)/((y(1)-x2)^2 + (y(3)-y2)^2+y(5)^2)^(3/2) ...
            + y(1)*Om^2                   + 2*Om*y(4)                   ;
    dy(4) = - M1*(y(3)-y1)/((y(1)-x1)^2 + (y(3)-y1)^2+y(5)^2)^(3/2) ...
            - M2*(y(3)-y2)/((y(1)-x2)^2 + (y(3)-y2)^2+y(5)^2)^(3/2) ...
            + y(3)*Om^2                   - 2*Om*y(2)                   ;
    dy(6) = - M1*(y(5))   /((y(1)-x1)^2 + (y(3)-y1)^2+y(5)^2)^(3/2) ...
            - M2*(y(5))   /((y(1)-x2)^2 + (y(3)-y2)^2+y(5)^2)^(3/2)   ;
            
    dy(1) =   y(2)                                                      ;   
    dy(3) =   y(4)                                                      ;
    dy(5) =   y(6)                                                      ;
    dy = dy';
end

